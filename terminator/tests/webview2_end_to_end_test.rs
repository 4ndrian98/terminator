use tokio::time::sleep;

#[tokio::test]
async fn test_webview2_end_to_end_workflow() -> Result<(), Box<dyn std::error::Error>> {
    println!("üß™ Testing END-TO-END WebView2 workflow: Open website ‚Üí Find element by ID ‚Üí Extract text");
    
    let desktop = Desktop::new(false, false)?;
    
    // Test with the original Dataiku website
    let target_url = "https://pages.dataiku.com/guide-to-ai-agents";
    let target_id = "hs_form_target_form_735002917";
    
    println!("üåê Opening Dataiku website: {}", target_url);
    let browser_element = match desktop.open_url(target_url, Some(Browser::Edge)) {
        Ok(element) => {
            println!("‚úÖ Browser opened successfully");
            element
        }
        Err(e) => {
            println!("‚ö†Ô∏è  Edge failed, trying default browser: {}", e);
            desktop.open_url(target_url, None)?
        }
    };
    
    println!("‚è≥ Waiting for page to load...");
    sleep(Duration::from_secs(8)).await;
    
    // Test 1: Basic WebView2 script execution
    println!("üß™ Test 1: Basic script execution (document.title)");
    let title_script = "document.title";
    
    match browser_element.execute_script(title_script) {
        Ok(Some(result)) => {
            println!("‚úÖ SUCCESS: Document title = '{}'", result);
        }
        Ok(None) => {
            println!("‚ö†Ô∏è  Script executed but no result returned");
        }
        Err(e) => {
            println!("‚ùå Basic script execution failed: {}", e);
            return Err(e.into());
        }
    }
    
    // Test 2: Check if target element exists by ID
    println!("üß™ Test 2: Finding element by ID: {}", target_id);
    let exists_script = format!("document.getElementById('{}') !== null", target_id);
    
    let element_exists = match browser_element.execute_script(&exists_script) {
        Ok(Some(result)) => {
            println!("üìä Element existence check result: '{}'", result);
            result == "true"
        }
        Ok(None) => {
            println!("‚ö†Ô∏è  Element existence check returned no result");
            false
        }
        Err(e) => {
            println!("‚ùå Element existence check failed: {}", e);
            false
        }
    };
    
    if element_exists {
        println!("‚úÖ SUCCESS: Element '{}' found on page!", target_id);
        
        // Test 3: Extract element content
        println!("üß™ Test 3: Extracting element content");
        let content_script = format!(
            "var elem = document.getElementById('{}'); elem ? elem.outerHTML : 'Element not found'", 
            target_id
        );
        
        match browser_element.execute_script(&content_script) {
            Ok(Some(html_content)) => {
                println!("üéâ SUCCESS: Element HTML extracted!");
                println!("üìã HTML Content: {}", 
                    if html_content.len() > 200 {
                        format!("{}... (truncated, total {} chars)", &html_content[..200], html_content.len())
                    } else {
                        html_content.clone()
                    }
                );
                
                // Verify it's actually HTML
                assert!(html_content.contains("<"), "Result should contain HTML tags");
                assert!(html_content.contains(&target_id), "HTML should contain the target ID");
            }
            Ok(None) => {
                println!("‚ö†Ô∏è  Content extraction returned no result");
                return Err("Content extraction failed".into());
            }
            Err(e) => {
                println!("‚ùå Content extraction failed: {}", e);
                return Err(e.into());
            }
        }
        
        // Test 4: Extract just the text content
        println!("üß™ Test 4: Extracting element text");
        let text_script = format!(
            "var elem = document.getElementById('{}'); elem ? elem.textContent || elem.innerText : 'No text'", 
            target_id
        );
        
        match browser_element.execute_script(&text_script) {
            Ok(Some(text_content)) => {
                println!("üéâ SUCCESS: Element text extracted!");
                println!("üìÑ Text Content: {}", 
                    if text_content.len() > 100 {
                        format!("{}... (truncated, total {} chars)", &text_content[..100], text_content.len())
                    } else {
                        text_content.clone()
                    }
                );
            }
            Ok(None) => {
                println!("‚ö†Ô∏è  Text extraction returned no result");
            }
            Err(e) => {
                println!("‚ùå Text extraction failed: {}", e);
            }
        }
        
    } else {
        println!("‚ÑπÔ∏è  Element '{}' not found on page", target_id);
        
        // Test alternative: Find any form elements
        println!("üß™ Alternative: Looking for any form elements");
        let forms_script = "document.forms.length";
        
        match browser_element.execute_script(forms_script) {
            Ok(Some(result)) => {
                println!("üìä Found {} forms on page", result);
            }
            Ok(None) => {
                println!("‚ö†Ô∏è  Form count check returned no result");
            }
            Err(e) => {
                println!("‚ùå Form count check failed: {}", e);
            }
        }
        
        // Try to find any element with 'form' in the ID
        let form_elements_script = "Array.from(document.querySelectorAll('[id*=\"form\"]')).length";
        
        match browser_element.execute_script(form_elements_script) {
            Ok(Some(result)) => {
                println!("üìä Found {} elements with 'form' in ID", result);
            }
            Ok(None) => {
                println!("‚ö†Ô∏è  Form element search returned no result");
            }
            Err(e) => {
                println!("‚ùå Form element search failed: {}", e);
            }
        }
    }
    
    println!("üéâ END-TO-END WebView2 workflow test completed!");
    println!("üìã Summary:");
    println!("   ‚úÖ Open website: SUCCESS");
    println!("   ‚úÖ Execute JavaScript: SUCCESS");
    println!("   {} Find element by ID: {}", 
        if element_exists { "‚úÖ" } else { "‚ö†Ô∏è" }, 
        if element_exists { "SUCCESS" } else { "ELEMENT NOT FOUND" });
    println!("   {} Extract content: {}", 
        if element_exists { "‚úÖ" } else { "‚ûñ" }, 
        if element_exists { "SUCCESS" } else { "SKIPPED" });
    
    Ok(())
}

#[tokio::test]
async fn test_webview2_simple_element_extraction() -> Result<(), Box<dyn std::error::Error>> {
    println!("üß™ Testing simple element extraction with real website");
    
    let desktop = Desktop::new(false, false)?;
    
    // Use example.com - a simple site with predictable structure
    let test_url = "https://example.com";
    
    println!("üåê Opening example.com");
    let browser_element = desktop.open_url(test_url, Some(Browser::Edge))?;
    
    println!("‚è≥ Waiting for page to load...");
    sleep(Duration::from_secs(3)).await;
    
    // Test basic element extraction from example.com
    println!("üß™ Test 1: Get page title");
    match browser_element.execute_script("document.title")? {
        Some(title) => println!("‚úÖ Page title: '{}'", title),
        None => println!("‚ùå No title returned"),
    }
    
    println!("üß™ Test 2: Find H1 element and extract text");
    let h1_text_script = "var h1 = document.querySelector('h1'); h1 ? h1.textContent : 'No H1 found'";
    match browser_element.execute_script(h1_text_script)? {
        Some(h1_text) => {
            println!("‚úÖ H1 text: '{}'", h1_text);
            // Clean quotes if present
            let clean_text = if h1_text.starts_with('"') && h1_text.ends_with('"') {
                &h1_text[1..h1_text.len()-1]
            } else {
                &h1_text
            };
            assert!(!clean_text.is_empty(), "H1 text should not be empty");
        }
        None => println!("‚ùå No H1 text returned"),
    }
    
    println!("üß™ Test 3: Find paragraph elements and extract text");
    let p_count_script = "document.querySelectorAll('p').length";
    match browser_element.execute_script(p_count_script)? {
        Some(count) => println!("‚úÖ Found {} paragraph elements", count),
        None => println!("‚ùå No paragraph count returned"),
    }
    
    let p_text_script = "var p = document.querySelector('p'); p ? p.textContent : 'No paragraph found'";
    match browser_element.execute_script(p_text_script)? {
        Some(p_text) => {
            println!("‚úÖ First paragraph text: '{}'", 
                if p_text.len() > 100 { 
                    format!("{}...", &p_text[..100]) 
                } else { 
                    p_text 
                }
            );
        }
        None => println!("‚ùå No paragraph text returned"),
    }
    
    println!("üß™ Test 4: Get complete HTML structure");
    let html_script = "document.documentElement.outerHTML";
    match browser_element.execute_script(html_script)? {
        Some(full_html) => {
            println!("‚úÖ Full HTML extracted ({} characters)", full_html.len());
            assert!(full_html.contains("<html"), "Should contain HTML structure");
            assert!(full_html.contains("<body"), "Should contain body element");
            
            // Show a snippet
            println!("üìã HTML snippet: {}", 
                if full_html.len() > 200 { 
                    format!("{}...", &full_html[..200]) 
                } else { 
                    full_html 
                }
            );
        }
        None => println!("‚ùå No HTML returned"),
    }
    
    println!("üéâ ANSWER TO YOUR QUESTION:");
    println!("   ‚úÖ CAN we open a website? YES - example.com opened successfully");
    println!("   ‚úÖ CAN we execute JavaScript? YES - multiple scripts executed");
    println!("   ‚úÖ CAN we find elements? YES - found H1, paragraphs, etc.");
    println!("   ‚úÖ CAN we extract text content? YES - extracted text from elements");
    println!("   ‚úÖ CAN we extract HTML content? YES - extracted full HTML structure");
    println!("   ‚úÖ CAN we return data to Rust? YES - all data returned as Rust strings");
    
    println!("üß™ Test 5: Simulate finding element by ID (like your original use case)");
    let simulate_id_test = r#"
        // Simulate what happens when looking for an element by ID
        var targetId = 'non-existent-id';
        var element = document.getElementById(targetId);
        if (element) {
            element.outerHTML;
        } else {
            'Element with ID "' + targetId + '" not found';
        }
    "#;
    
    match browser_element.execute_script(simulate_id_test)? {
        Some(result) => {
            println!("‚úÖ ID lookup simulation result: '{}'", result);
        }
        None => {
            println!("‚ùå ID lookup simulation failed");
        }
    }
    
    println!("üéâ Simple element extraction test completed!");
    Ok(())
}